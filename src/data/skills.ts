// Helper functions
export const getFeaturedSkills = () =>
  skills.filter((skill) => skill.featured).sort((a, b) => a.order - b.order)

export const getSkillBySlug = (slug: string) =>
  skills.find((skill) => skill.slug === slug)

export const getSkillsByCategory = (category: string) =>
  skills.filter((skill) => skill.category === category)

export const getAllSkillCategories = () =>
  [...new Set(skills.map((skill) => skill.category))]

export const getAllSkillSlugs = () =>
  skills.map((skill) => skill.slug)
